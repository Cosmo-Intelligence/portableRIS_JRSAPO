// 受付者情報
select
	m.USERID,
	m.USERNAME
from
	USERMANAGE m,
	USERINFO_CA i
where
	i.LOGINID = m.USERID
and
	i.SYOKUIN_KBN in ('設定ファイル値')
order by
	i.SHOWORDER asc


// 病棟情報
select
	BYOUTOU_ID,
	BYOUTOU_NAME
from
	BYOUTOUMASTER
where
	USEFLAG = '1'
order by
	SHOWORDER asc


// 検査種別情報
select
	KENSATYPE_ID,
	KENSATYPE_NAME
from
	KENSATYPEMASTER
where
	KENSATYPE_ID in ('設定ファイル値')
and
	USEFLAG = '1'
order by
	SHOWORDER asc


// 検査機器情報
